"""
Простая морфология русских слов для региональных фильтров.

Цель — без сторонних библиотек (pymorphy2 и т.п.) получать стем русского
существительного/прилагательного, чтобы поиск ключевого слова в тексте
ловил все падежные/родовые формы одного корня.

Пример:
    >>> get_word_stem("Малмыжский")
    'малмыж'
    >>> text_matches_keyword("В Малмыже прошёл фестиваль", "Малмыжский")
    True
"""
import re
from typing import Iterable, List, Set


# Окончания (русские падежные/родовые + адъективные суффиксы), длинные первыми.
# Усечение применяется только если оставшийся стем ≥ MIN_STEM_LEN.
#
# Сначала идут составные формы прилагательного (суффикс «ск/еск/инск» + падеж),
# чтобы «Малмыжский» давало «малмыж», а не «малмыжск». Это даёт совпадение
# с короткой формой «Малмыже» в тексте.
_ENDINGS: List[str] = sorted(
    {
        # ----- Адъективные суффиксы + падежные окончания (5–6 букв) -----
        "овскими", "евскими", "инскими", "анскими", "ынскими",
        "овского", "евского", "инского", "анского", "ынского",
        "ческими", "ческого", "ческому", "ческими",
        # ----- Адъективные суффиксы + падежи (4–5 букв) -----
        "овский", "евский", "инский", "анский", "ынский",
        "овская", "евская", "инская", "анская", "ынская",
        "овское", "евское", "инское", "анское", "ынское",
        "овские", "евские", "инские", "анские", "ынские",
        "ческий", "ческая", "ческое", "ческие",
        "скими", "ского", "ской", "скому", "ским", "ских", "скую", "ском",
        "ский", "ская", "ское", "ские",
        # ----- Просто падежные окончания (3 буквы) -----
        "ыми", "ими", "ого", "его", "ому", "ему", "ами", "ями", "иями",
        # ----- 2 буквы (родовые/падежные) -----
        "ый", "ий", "ой", "ая", "яя", "ое", "ее", "ые", "ие",
        "ом", "ем", "ам", "ям", "ах", "ях", "ов", "ев", "ёв",
        # ----- 1 буква -----
        "а", "я", "у", "ю", "о", "е", "и", "ы", "ь", "й",
    },
    key=len,
    reverse=True,
)

MIN_STEM_LEN = 4  # короче — стемом не считаем, оставляем слово как есть
_WORD_RE = re.compile(r"[A-Za-zА-Яа-яЁё0-9_]+", re.UNICODE)


def get_word_stem(word: str) -> str:
    """Вернуть простой стем русского слова без сторонних либ.

    Идея: режем самые длинные распространённые окончания, пока оставшаяся
    часть имеет длину ≥ MIN_STEM_LEN. Для коротких слов и слов без
    русских окончаний возвращаем слово как есть.
    """
    if not word:
        return ""
    w = word.lower().strip().replace("ё", "е")
    if len(w) <= MIN_STEM_LEN:
        return w
    for ending in _ENDINGS:
        if w.endswith(ending) and len(w) - len(ending) >= MIN_STEM_LEN:
            return w[: -len(ending)]
    return w


def expand_keywords(keywords: Iterable[str]) -> Set[str]:
    """Получить множество стемов для входных ключевых слов.

    На вход — произвольные формы (например, «Малмыжский», «Малмыж», «Кукмор»).
    На выходе — стемы, по которым удобно искать в нормализованном тексте.
    Пустые/None-значения игнорируем.
    """
    stems: Set[str] = set()
    for kw in keywords or ():
        if not kw or not isinstance(kw, str):
            continue
        stem = get_word_stem(kw)
        if stem and len(stem) >= MIN_STEM_LEN:
            stems.add(stem)
    return stems


def text_matches_keyword(text: str, keyword: str) -> bool:
    """Проверить, встречается ли слово (в любой морф. форме) в тексте.

    Сопоставление — по началу слова: токенизуем текст по границам и
    смотрим, начинается ли какой-то токен с стема ключевого слова.
    Это исключает ложные срабатывания на подстроке внутри другого слова.
    """
    if not text or not keyword:
        return False
    stem = get_word_stem(keyword)
    if not stem:
        return False
    normalized = text.lower().replace("ё", "е")
    for token in _WORD_RE.findall(normalized):
        if token.startswith(stem):
            return True
    return False


def find_matching_keywords(text: str, keywords: Iterable[str]) -> List[str]:
    """Вернуть список ключевых слов, для которых нашлась морф. форма в тексте.

    Сохраняем исходный регистр и форму ключевого слова — это удобно для
    логирования причины пропуска поста через фильтр.
    """
    if not text:
        return []
    normalized = text.lower().replace("ё", "е")
    tokens = _WORD_RE.findall(normalized)
    if not tokens:
        return []
    matched: List[str] = []
    for kw in keywords or ():
        if not kw or not isinstance(kw, str):
            continue
        stem = get_word_stem(kw)
        if not stem:
            continue
        if any(tok.startswith(stem) for tok in tokens):
            matched.append(kw)
    return matched
