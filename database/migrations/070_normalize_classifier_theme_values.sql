-- Вердикты: тема через lower(trim) по карте; канонические имена (уже русские,
-- в т.ч. с большой буквы: «Образование», «Православие» и т.п.) — просто lower.
UPDATE content_classifications cc
SET verdict = jsonb_set(cc.verdict::jsonb, '{theme}', to_jsonb(m.canon))::json
FROM theme_map m
WHERE lower(btrim(cc.verdict->>'theme')) = m.old;

UPDATE content_classifications cc
SET verdict = jsonb_set(cc.verdict::jsonb, '{theme}', to_jsonb(lower(btrim(cc.verdict->>'theme'))))::json
WHERE lower(btrim(cc.verdict->>'theme')) IN
      (SELECT name FROM classifier_themes)
  AND cc.verdict->>'theme' <> lower(btrim(cc.verdict->>'theme'));

-- Правки оператора по теме — той же картой.
UPDATE classification_corrections c
SET operator_value = to_jsonb(m.canon)::json
FROM theme_map m
WHERE c.verdict_type = 'theme'
  AND lower(btrim(c.operator_value#>>'{}')) = m.old;

UPDATE classification_corrections c
SET operator_value = to_jsonb(lower(btrim(c.operator_value#>>'{}')))::json
WHERE c.verdict_type = 'theme'
  AND lower(btrim(c.operator_value#>>'{}')) IN (SELECT name FROM classifier_themes)
  AND c.operator_value#>>'{}' <> lower(btrim(c.operator_value#>>'{}'));

